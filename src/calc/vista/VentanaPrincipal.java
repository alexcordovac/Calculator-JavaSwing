    private void limpiarPantalla(){
       this.txtPantalla.setText("");
       this.txtTemp.setText("");
       txtPantalla.requestFocus();
    }
    
    private void pintarNumero(char n){
        if(esResultado){
            System.out.println("es resultado");
            this.btnBorrar.doClick();
            esResultado = false;
        }
        String text = this.txtPantalla.getText();
        if(!text.contains("-") || n!='-'){
            this.txtPantalla.setText(text + n);
            txtPantalla.requestFocus();            
        }
        
    }
    
    private void borrarCaracter(){
        String text = this.txtPantalla.getText();
        if(text.length()>0){
            
            if(text.equalsIgnoreCase("infinity")){
                this.limpiarPantalla();           
            }
            if(esResultado){
                this.limpiarPantalla();
                this.btnBorrar.doClick();
                esResultado = false;
            }else{
                this.txtPantalla.setText(text.substring(0, text.length()-1));
            }
            
        }
        txtPantalla.requestFocus();
    }
    
    private void ponerEnMemoria(String operador){
        try{
            String text = this.txtPantalla.getText();
            if((!text.equalsIgnoreCase("NaN")) && (!text.equalsIgnoreCase("infinity"))){
                
                if(text.length() > 0)
                    this.servicio.setTemp(Double.valueOf(text));
                else 
                    this.servicio.setTemp(0);
                
                this.servicio.setUltimoOperador(operador);
                this.limpiarPantalla();
                this.esResultado = false;
                this.txtTemp.setText((text.length()>0 ? text : '0') + " " + operador);
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Error: numero no valido. " + e.getMessage());
        }
        txtPantalla.requestFocus();
        }
        
    private void obtenerResultado(){
        try{
            String text = this.txtPantalla.getText();
            String operador = this.servicio.getUltimoOperador();
            
            if(text.length()>0 && operador!=null){
                double resultado = this.servicio.calcular(Double.valueOf(text));
                this.txtPantalla.setText(String.valueOf(resultado));
                this.servicio.setUltimoOperador(null);
                esResultado = true;
                this.txtTemp.setText(this.txtTemp.getText() + " " + text + " =");
                this.servicio.setTemp(Double.NaN);
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Error: numero no valido");
        }
        txtPantalla.requestFocus();
    }
    
